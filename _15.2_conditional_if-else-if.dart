void main() {
  var val = 10;
  var inputnum = 5;
  if (inputnum > val) {
    print("$inputnum is greater than $val");
  } else if (inputnum < val) {
    print("$inputnum is lesser than $val");
  } else {
    print("$inputnum is equal to $val");
  }
}

/* 
Case 1 - 20 is greater than 10
Case 2 - 10 is equal to 10
Case 3 - 5 is lesser than 10
 */